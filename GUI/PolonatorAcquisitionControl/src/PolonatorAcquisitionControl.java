        protected void done(){
            if(appTab.equals("flUtils")){
                enableAllFlUtilButtons();
            }
            else if(appTab.equals("acqUtils")){
                enableAllAcqUtilButtons();
                if(homing_axis.isSelected()){
                    polonatorCycleEntryValidate.setEnabled(true);
                    polonatorTouch.setEnabled(true);
                }
            }
            else if(appTab.equals("acqUtilsLive")){
                enableAllAcqUtilsCameraButtons();
                utilFocusBar.setEnabled(false);
                utilSetFocus.setEnabled(false);
            }
            else if(appTab.equals("focus")){
                try{
                    Integer i = get();
                    utilFocusLabel.setText(i.toString());
                    utilFocusBar.setValue(i);
                } catch (Exception ignore){}
            }
            else if(appTab.equals("acq")){
                jf.setDefaultCloseOperation(EXIT_ON_CLOSE);
                enableALLButtons();
            }
            else if(appTab.equals("SBS")){
                enableSBSButtons();
            }
            else if(appTab.equals("polonatorStart")){
                enableALLButtons();
                polonatorCycleEntry.setEnabled(true);
                polonatorCycleEntryValidate.setEnabled(true);
                polonatorTouch.setEnabled(true);
                jf.setDefaultCloseOperation(EXIT_ON_CLOSE);
            }
            else if(execCommand.equals(acqbase_dir + "/process_kill.pl acq")){
                    if(homing_axis.isSelected()){
                        disableAllAcqUtilButtons();
                        polonatorCycleEntryValidate.setEnabled(false);
                        polonatorTouch.setEnabled(false);
                        SwingWorker<Integer, String> worker2 = createWorker(acqbase_dir + "/PolonatorUtils reset", polonatorTextArea, "acqUtils");
                        worker2.execute();
                    }
            }
        }
